#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dest;
	if (src < dest)
	{
		s = s + n - 1;
		d = d + n - 1;
		while (n--)
		*d-- = *s--;
	}
	else if (src >= dest)
	{
		while (n--)
			*d++ = *s++;
	}
	return (dest);
}

